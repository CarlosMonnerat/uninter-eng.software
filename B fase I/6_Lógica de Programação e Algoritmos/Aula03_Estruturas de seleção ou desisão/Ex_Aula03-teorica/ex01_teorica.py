'''
Exercício 01: Desenvolva um programa que lê dois valores numéricos inteiros e compara se o primeiro é maior do que o
segundo, utilizando uma condicional simples.
Caso seja (resultado verdadeiro), ele imprime na tela a mensagem informando que o primeiro valor digitado é maior do
que o segundo.
'''

n1 = int(input('Digite um número inteiro: '))
n2 = int(input('Digite outro número inteiro: '))

if(n1 > n2):
    print('O primeiro valor digitado é maior que o segundo.')
